public class Task5_InchToCm {


    public static void main(String[] args) {
        byte inch_0 = 12;
        float inch_1 = 23.23f;
        final float inchToCm = 2.54f;
        
        System.out.println("12 inches = " + inch_0*inchToCm + " centimeters");
        System.out.println("23.23 inches = " + inch_1*inchToCm + " centimeters");
    }
    
}
